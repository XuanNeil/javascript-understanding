/**
 *
 * toUpperCase() methods returns the calling string value converted to uppercase.
 *
 * return:
 *  - A new string representing the calling string converted to upper case.
 *  Exceptions:
 *  - TypeError: When call on null or undefined.
 *  - vd: String.prototype.toUpperCase.call(undefined);
 */

const text = "This is a text";
console.log(text.toUpperCase());