/**
 * The String object's charAt() method returns a new string consisting of the single UTF-16 code unit located
 * at the specified offset into the string.
 *
 */

const text = "This is text";
console.log(text.charAt(0));

const anyString = 'Brave new world';
console.log(anyString.charAt());// position: 0

console.log(anyString.charAt(100)); // ''

